public class Calculadora {
    //metodo para somar dois valores corretamente
    public int soma(int x, int y){
        return x + y;
    }
    //metodo subtração de dois numeros
    public int subtracao(int x, int y){
        return x - y;
    }
    //metodo multiplicacao de dois numeros
    public int multiplicacao(int x, int y){
        return x * y;
    }
    //metodo divisao de dois numeros
    public int divisao(int x, int y){
        return x / y;
    }
}
